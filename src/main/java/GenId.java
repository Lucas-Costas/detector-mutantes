public class GenId {

    private Integer indexHorizontal;

    private Integer indexVertical;

    public GenId(Integer indexHorizontal, Integer indexVertical) {
        this.indexHorizontal = indexHorizontal;
        this.indexVertical = indexVertical;
    }

    public Integer getIndexHorizontal() {
        return indexHorizontal;
    }

    public Integer getIndexVertical() {
        return indexVertical;
    }

}
